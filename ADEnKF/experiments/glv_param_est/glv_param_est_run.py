# ---------------------------------------------------------------------------
# gLV parameterised ODE (learned model)
# ---------------------------------------------------------------------------

class GLVParamODE(nn.Module):
    """
    Parameterised gLV ODE matching the generator convention:

        dx_i/dt = x_i * (r_i + sum_j A_ij * x_j)

    Parameters are stored as a 1D vector theta with labels:
        ['r_1', ..., 'r_5', 'a_11', 'a_13', ...]

    The labels and true theta are loaded from the gLV truth .npz file
    produced by `glv_data_generator.generate_experiment`.
    """

    def __init__(self, theta_init: torch.Tensor, theta_labels: List[str], x_dim: int = 5):
        super().__init__()
        if theta_init.ndim != 1:
            raise ValueError("theta_init must be a 1D tensor.")
        self.theta = nn.Parameter(theta_init.clone())
        self.theta_labels = list(theta_labels)
        self.x_dim = x_dim

        # Pre-compute index mapping from theta -> (r, A)
        r_indices: List[int] = []
        A_param_indices: List[int] = []
        A_rows: List[int] = []
        A_cols: List[int] = []

        for idx, lbl in enumerate(self.theta_labels):
            lbl_str = lbl.decode("utf-8") if isinstance(lbl, bytes) else str(lbl)
            if lbl_str.startswith("r_"):
                r_indices.append(idx)
            elif lbl_str.startswith("a_"):
                # labels are of form 'a_ij' with 1-based indices
                try:
                    i = int(lbl_str[2]) - 1
                    j = int(lbl_str[3]) - 1
                except Exception as exc:
                    raise ValueError(f"Unrecognised theta label '{lbl_str}'") from exc
                A_param_indices.append(idx)
                A_rows.append(i)
                A_cols.append(j)

        if len(r_indices) != x_dim:
            raise ValueError(
                f"Expected {x_dim} growth-rate parameters r_i, got {len(r_indices)}."
            )

        device = theta_init.device
        self.register_buffer("_r_idx", torch.tensor(r_indices, dtype=torch.long, device=device))
        self.register_buffer(
            "_A_param_idx", torch.tensor(A_param_indices, dtype=torch.long, device=device)
        )
        self.register_buffer("_A_rows", torch.tensor(A_rows, dtype=torch.long, device=device))
        self.register_buffer("_A_cols", torch.tensor(A_cols, dtype=torch.long, device=device))

    def _unpack_theta(self) -> Tuple[torch.Tensor, torch.Tensor]:
        """
        Returns:
            r : (x_dim,)
            A : (x_dim, x_dim)
        """
        theta = self.theta
        r = theta[self._r_idx]  # (x_dim,)

        A = torch.zeros(self.x_dim, self.x_dim, device=theta.device, dtype=theta.dtype)
        if self._A_param_idx.numel() > 0:
            A[self._A_rows, self._A_cols] = theta[self._A_param_idx]
        return r, A

    def forward(self, t, u):
        # u: (*batch, x_dim)
        r, A = self._unpack_theta()
        # Ensure non-negativity of populations
        x = torch.clamp(u, min=0.0)
        # RHS: x * (r + A @ x)
        # r broadcasts over batch dimension.
        interaction = torch.matmul(x, A.t())
        dx = x * (r + interaction)
        return dx


# ---------------------------------------------------------------------------
# Data loading from gLV .npz files
# ---------------------------------------------------------------------------

def _load_glv_data(cfg: DictConfig, device):
    """
    Load truth and observations from EnKF_PPE gLV .npz files.

    Config fields:
        data_truth_path: relative to Data/ (e.g. gLV/data/glv_ahidden0p00_truth.npz)
        data_obs_path  : relative to Data/ (e.g. gLV/data/glv_ahidden0p00_obs.npz)
    """
    truth_path = DATA_DIR / cfg.data_truth_path
    obs_path = DATA_DIR / cfg.data_obs_path

    if not truth_path.is_file():
        raise FileNotFoundError(f"gLV truth file not found: {truth_path}")
    if not obs_path.is_file():
        raise FileNotFoundError(f"gLV obs file not found: {obs_path}")

    truth_np = np.load(truth_path)
    obs_np = np.load(obs_path)

    t = truth_np["t"].astype(np.float32)  # (T,)
    X = truth_np["X"].astype(np.float32)  # (N, T)
    theta_true_np = truth_np["theta"].astype(np.float32)  # (p_dim,)
    theta_labels_np = truth_np["theta_labels"]
    x0_np = truth_np["x0"].astype(np.float32)  # (N,)

    Y = obs_np["Y"].astype(np.float32)  # (M, T)
    H_np = obs_np["H"].astype(np.float32)  # (M, N)
    obs_noise_std = float(obs_np["obs_noise_std"])

    # Time grid and observation truncation (n_obs hyperparameter)
    n_obs_full = t.shape[0]
    n_obs_cfg = int(getattr(cfg, "n_obs", n_obs_full))
    n_obs = min(n_obs_cfg, n_obs_full)

    t = t[:n_obs]
    X = X[:, :n_obs]
    Y = Y[:, :n_obs]

    dt = float(t[1] - t[0]) if t.shape[0] > 1 else float(cfg.dt)
    t_obs = torch.from_numpy(t).to(device)  # (n_obs,)

    # Truth and observations as (n_obs, 1, dim)
    X_torch = torch.from_numpy(X.T).to(device)  # (T, N)
    Y_torch = torch.from_numpy(Y.T).to(device)  # (T, M)

    truth = X_torch.unsqueeze(1)  # (n_obs, 1, x_dim)
    y_obs = Y_torch.unsqueeze(1)  # (n_obs, 1, y_dim)

    x_dim = X.shape[0]
    y_dim = Y.shape[0]

    initial_state = torch.from_numpy(x0_np).to(device)  # (x_dim,)
    H = torch.from_numpy(H_np).to(device)  # (y_dim, x_dim)
    theta_true = torch.from_numpy(theta_true_np).to(device)  # (p_dim,)
    theta_labels = [
        lbl.decode("utf-8") if isinstance(lbl, bytes) else str(lbl) for lbl in theta_labels_np
    ]

    return (
        truth,
        y_obs,
        theta_true,
        theta_labels,
        initial_state,
        t_obs,
        dt,
        H,
        obs_noise_std,
        x_dim,
        y_dim,
        n_obs,
    )



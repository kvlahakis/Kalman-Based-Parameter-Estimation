@hydra.main(config_path="configs", config_name="glv_param_est", version_base=None)
def run(cfg: DictConfig) -> None:
    from hydra.core.hydra_config import HydraConfig

    device = _setup_device()
    print(f"device: {device}")

    run_dir = Path(HydraConfig.get().runtime.output_dir)
    run_dir.mkdir(parents=True, exist_ok=True)
    log_path = run_dir / "glv_param_est_run.log"
    log_file = open(log_path, "w", encoding="utf-8")

    def log(msg: str = ""):
        log_file.write(msg + "\n")
        log_file.flush()

    log("=" * 60)
    log("gLV AD-EnKF parameter estimation run")
    log("=" * 60)
    log(f"  data_truth_path   = {cfg.get('data_truth_path', 'N/A')}")
    log(f"  data_obs_path     = {cfg.get('data_obs_path', 'N/A')}")
    log(f"  init_theta_scale = {cfg.get('init_theta_scale')}")
    log(f"  lr                = {cfg.get('lr')}")
    log(f"  process_std       = {cfg.get('process_std')}")
    log(f"  N_ens             = {cfg.N_ens}")
    log(f"  epochs            = {cfg.epochs}")
    log(f"  chunk_length      = {cfg.chunk_length}")
    log("=" * 60)
    log(f"{'Epoch':>6}  {'||θ-θ_true||_2':>16}  {'Log-likelihood':>16}  {'tr(C_ens)':>12}  {'min_std_dim':>12}")
    log("-" * 60)

    torch.manual_seed(cfg.seed)
    np.random.seed(cfg.seed)
    # Python's random not strictly needed here, but keep for parity with L63.
    import random as _random

    _random.seed(cfg.seed)

    (
        truth,
        y_obs,
        theta_true,
        theta_labels,
        initial_state,
        t_obs,
        dt,
        H,
        obs_noise_std,
        x_dim_data,
        y_dim_data,
        n_obs,
    ) = _load_glv_data(cfg, device)

    # Sanity check vs config
    x_dim = int(cfg.x_dim)
    if x_dim != x_dim_data:
        raise ValueError(f"x_dim in config ({x_dim}) != data dimension ({x_dim_data})")
    y_dim = y_dim_data

    N_ensem = cfg.N_ens
    L = cfg.chunk_length
    t0 = 0.0
    train_size = 1  # single gLV trajectory per dataset

    # Observation operator and noise (use H and obs_noise_std from the data file)
    H_true = H.to(device)
    true_obs_func = nn_templates.Linear(x_dim, y_dim, H=H_true).to(device)
    noise_R_true = noise.AddGaussian(
        y_dim, torch.tensor(obs_noise_std, device=device), param_type="scalar"
    ).to(device)

    # Initial ensemble mean: start around the data-generating initial state
    init_m = initial_state

    init_C_param = noise.AddGaussian(
        x_dim,
        torch.diag(torch.tensor(cfg.init_cov_diag, device=device)),
        "full",
    ).to(device)

    # Parameter initialisation: scaled version of truth (keeps sparsity pattern)
    init_theta_scale = float(cfg.init_theta_scale)
    if init_theta_scale < 1e-6:
        raise ValueError(
            "init_theta_scale must be > 0 (e.g. 0.5 or 1.0). "
            "Zero gives degenerate gLV dynamics (dx/dt=0) and leads to NaN."
        )
    init_theta = theta_true * init_theta_scale
    learned_ode_func = GLVParamODE(init_theta, theta_labels, x_dim=x_dim).to(device)

    # Process noise Q: per-dimension std
    q_scale0 = float(cfg.get("process_std", cfg.get("init_Q_diag_scale", 0.05)))
    init_Q = q_scale0 * torch.ones(x_dim, device=device)
    learned_model_Q = noise.AddGaussian(x_dim, init_Q, "diag").to(device)

    optimizer = torch.optim.Adam(
        [
            {"params": learned_ode_func.parameters(), "lr": cfg.lr},
            {"params": learned_model_Q.parameters(), "lr": cfg.lr},
        ]
    )

    epoch_start = cfg.get("lr_lambda_epoch_start", 30)
    expo = cfg.get("lr_lambda_exponent", -0.4)

    def lr_lambda(epoch):
        return (epoch - 9) ** expo if epoch >= epoch_start else 1.0

    scheduler = torch.optim.lr_scheduler.LambdaLR(
        optimizer,
        lr_lambda=[lr_lambda, lr_lambda],
    )

    monitor = []  # each entry: [theta..., q_scale, log_likelihood]
    filter_mode = str(cfg.get("filter_mode", "ad")).lower()  # "ad" or "em"

    for epoch in tqdm(range(cfg.epochs), desc="Training", leave=False):
        train_log_likelihood = torch.zeros(train_size, device=device)
        t_start = t0
        X = init_C_param(init_m.expand(train_size, N_ensem, x_dim))

        for start in range(0, n_obs, L):
            optimizer.zero_grad()
            end = min(start + L, n_obs)
            if filter_mode == "ad":
                X, _, log_likelihood = da_methods.EnKF(
                    learned_ode_func,
                    true_obs_func,
                    t_obs[start:end],
                    y_obs[start:end],
                    N_ensem,
                    init_m,
                    init_C_param,
                    learned_model_Q,
                    noise_R_true,
                    device,
                    save_filter_step={},
                    t0=t_start,
                    init_X=X,
                    ode_method=cfg.ode_method,
                    ode_options=dict(step_size=cfg.ode_step_size),
                    adjoint=True,
                    adjoint_method=cfg.adjoint_method,
                    adjoint_options=dict(step_size=cfg.adjoint_step_size),
                    tqdm=None,
                )
            else:
                X, _, log_likelihood = EnKF_EM(
                    learned_ode_func,
                    true_obs_func,
                    t_obs[start:end],
                    y_obs[start:end],
                    N_ensem,
                    init_m,
                    init_C_param,
                    learned_model_Q,
                    noise_R_true,
                    device,
                    init_X=X,
                    ode_method=cfg.ode_method,
                    ode_options=dict(step_size=cfg.ode_step_size),
                    t0=float(t_start),
                    compute_likelihood=True,
                    tqdm=None,
                )
            t_start = t_obs[end - 1]
            (-log_likelihood).mean().backward()
            train_log_likelihood += log_likelihood.detach().clone()
            # Gradient clipping to avoid NaN from exploding gradients (gLV is sensitive)
            clip_norm = cfg.get("clip_grad_norm")
            if clip_norm is not None and float(clip_norm) > 0:
                torch.nn.utils.clip_grad_norm_(
                    list(learned_ode_func.parameters()) + list(learned_model_Q.parameters()),
                    max_norm=float(clip_norm),
                )
            optimizer.step()

        scheduler.step()

        with torch.no_grad():
            l2_err = float(torch.norm(learned_ode_func.theta - theta_true).item())
            ll_val = train_log_likelihood.mean().item()
            q_scale = torch.sqrt(torch.trace(learned_model_Q.full()) / x_dim)
            theta_cpu = learned_ode_func.theta.detach().cpu().numpy().tolist()

            # Ensemble spread (collapse monitoring): X shape (train_size, N_ensem, x_dim)
            X_mean = X.mean(dim=1, keepdim=True)
            X_ct = X - X_mean
            var_per_dim = (X_ct ** 2).sum(dim=1) / max(N_ensem - 1, 1)  # (train_size, x_dim)
            trace_ens_cov = float(var_per_dim.sum().item())
            min_std_dim = float((var_per_dim.clamp(min=1e-20).min().item()) ** 0.5)

            monitor.append(theta_cpu + [q_scale.item(), ll_val, trace_ens_cov, min_std_dim])

        # Write every epoch to log file (formatted)
        log(f"{epoch:6d}  {l2_err:16.6e}  {ll_val:16.4f}  {trace_ens_cov:12.4e}  {min_std_dim:12.4e}")

        if epoch % 10 == 0:
            tqdm.write(
                f"Epoch {epoch} | LL: {ll_val:.2f} | "
                f"||θ-θ_true||_2 = {l2_err:.3e} | "
                f"tr(C_ens) = {trace_ens_cov:.3e} | min_std_dim = {min_std_dim:.3e}"
            )

    # ----------------------------------------------------------------------
    # Training summary: write to log file and run_summary.yaml for sweep comparison
    # ----------------------------------------------------------------------
    monitor_arr = np.asarray(monitor)
    p_dim = theta_true.numel()
    # monitor columns: theta (p_dim), q_scale, ll, trace_ens_cov, min_std_dim
    if monitor_arr.shape[1] != p_dim + 4:
        raise RuntimeError(
            f"Monitor has unexpected shape {monitor_arr.shape}; expected (*, {p_dim + 4})."
        )

    with torch.no_grad():
        final_l2_error = float(torch.norm(learned_ode_func.theta - theta_true).item())
    final_ll = float(monitor_arr[-1, p_dim + 1])
    best_ll = float(monitor_arr[:, p_dim + 1].max())
    best_ll_epoch = int(monitor_arr[:, p_dim + 1].argmax())
    final_trace_ens = float(monitor_arr[-1, p_dim + 2])
    final_min_std_dim = float(monitor_arr[-1, p_dim + 3])
    min_std_dim_over_time = float(monitor_arr[:, p_dim + 3].min())

    log("-" * 60)
    log("Summary")
    log("-" * 60)
    log(f"  Final  ||θ - θ_true||_2  = {final_l2_error:.6e}")
    log(f"  Final  log-likelihood   = {final_ll:.4f}")
    log(f"  Best   log-likelihood   = {best_ll:.4f}  (epoch {best_ll_epoch})")
    log("  Ensemble spread (collapse check):")
    log(f"    Final  tr(C_ens)     = {final_trace_ens:.6e}")
    log(f"    Final  min_std_dim   = {final_min_std_dim:.6e}")
    log(f"    Minimum min_std_dim  = {min_std_dim_over_time:.6e}  (over all epochs)")
    log("=" * 60)
    log_file.close()

    summary = {
        "final_l2_error": final_l2_error,
        "final_ll": final_ll,
        "best_ll": best_ll,
        "best_ll_epoch": best_ll_epoch,
        "final_ensemble_trace_cov": final_trace_ens,
        "final_min_std_dim": final_min_std_dim,
        "min_min_std_dim": min_std_dim_over_time,
        "epochs": int(cfg.epochs),
        "data_truth_path": str(cfg.get("data_truth_path", "")),
        "data_obs_path": str(cfg.get("data_obs_path", "")),
        "init_theta_scale": float(cfg.get("init_theta_scale", 1.0)),
        "lr": float(cfg.get("lr", 0.01)),
        "process_std": float(cfg.get("process_std", 0.01)),
        "N_ens": int(cfg.N_ens),
        "chunk_length": int(cfg.chunk_length),
    }
    summary_path = run_dir / "run_summary.yaml"
    with open(summary_path, "w", encoding="utf-8") as f:
        OmegaConf.save(OmegaConf.create(summary), f)
    print(f"Run summary written to {summary_path}")

    # ----------------------------------------------------------------------
    # Write final r and A estimates in same format as glv_data_generator.py
    # ----------------------------------------------------------------------
    with torch.no_grad():
        r_est, A_est = learned_ode_func._unpack_theta()
        r_np = r_est.detach().cpu().numpy()
        A_np = A_est.detach().cpu().numpy()

    params_path = run_dir / "glv_estimated_params.py"
    with open(params_path, "w", encoding="utf-8") as f:
        f.write('"""\n')
        f.write("gLV parameter estimates from AD-EnKF (glv_param_est_run).\n")
        f.write("Same format as Data/gLV/glv_data_generator.py: R = growth rates, A = interaction matrix.\n")
        f.write("Convention: A[i,j] = effect of species j on species i's per-capita growth rate.\n")
        f.write('"""\n\n')
        f.write("import numpy as np\n\n")
        f.write("# Intrinsic growth rates (r_1 .. r_5)\n")
        f.write("R_EST = np.array([")
        f.write(", ".join(f"{x:.6g}" for x in r_np))
        f.write("])\n\n")
        f.write("# Interaction matrix A[i,j] = effect of j on i (same layout as A_TRUE in glv_data_generator)\n")
        f.write("# fmt: off\n")
        f.write("A_EST = np.array([\n")
        row_comments = [
            "  # prod1:    self-reg; loss to herb3",
            "  # prod2:    self-reg; loss to herb4",
            "  # herb3:    gain from prod1; loss to predator",
            "  # herb4:    gain from prod2; loss to predator",
            "  # predator: gain from herb3 and herb4",
        ]
        for i in range(5):
            row = ", ".join(f"{A_np[i, j]:8.4f}" for j in range(5))
            f.write(f"    [{row}],{row_comments[i]}\n")
        f.write("])\n")
        f.write("# fmt: on\n")
    print(f"Estimated parameters (R_EST, A_EST) written to {params_path}")

    true_vals = theta_true.detach().cpu().numpy().tolist()
    param_names = list(theta_labels)

    import matplotlib

    matplotlib.use("Agg")
    import matplotlib.pyplot as plt

    fig, axes = plt.subplots(1, 4, figsize=(20, 4))

    # Panel 0: all parameters vs epoch
    ax0 = axes[0]
    for i, (name, true_val) in enumerate(zip(param_names, true_vals)):
        line, = ax0.plot(monitor_arr[:, i], label=name)
        ax0.axhline(
            true_val,
            color=line.get_color(),
            linestyle="--",
            alpha=0.5,
        )
    ax0.set_title("gLV parameter convergence")
    ax0.set_xlabel("Epoch")
    ax0.set_ylabel("Parameter value")
    ax0.legend(fontsize=6, ncol=3)

    # Panel 1: model noise magnitude
    axes[1].plot(monitor_arr[:, p_dim])
    axes[1].set_title("Model noise scale (q)")
    axes[1].set_xlabel("Epoch")
    axes[1].set_ylabel("q")

    # Panel 2: training log-likelihood
    axes[2].plot(monitor_arr[:, p_dim + 1])
    axes[2].set_title("Training log-likelihood")
    axes[2].set_xlabel("Epoch")
    axes[2].set_ylabel("Log-likelihood")

    # Panel 3: ensemble spread (collapse monitoring)
    ax3 = axes[3]
    ax3.plot(monitor_arr[:, p_dim + 2], label="tr(C_ens)", color="C0")
    ax3.set_ylabel("tr(C_ens)", color="C0")
    ax3.tick_params(axis="y", labelcolor="C0")
    ax3_twin = ax3.twinx()
    ax3_twin.plot(monitor_arr[:, p_dim + 3], label="min_std_dim", color="C1")
    ax3_twin.set_ylabel("min_std_dim", color="C1")
    ax3_twin.tick_params(axis="y", labelcolor="C1")
    ax3.set_title("Ensemble spread (collapse check)")
    ax3.set_xlabel("Epoch")
    ax3.set_yscale("log")
    ax3_twin.set_yscale("log")

    plt.tight_layout()

    plot_path = Path(cfg.get("plot_path", "glv_parameter_estimation.png"))
    if not plot_path.is_absolute():
        plot_path = run_dir / plot_path
    plot_path.parent.mkdir(parents=True, exist_ok=True)
    plt.savefig(plot_path, dpi=150)
    plt.close()
    print(f"Parameter-estimation plot saved to {plot_path}")

    # ----------------------------------------------------------------------
    # Save results for downstream analysis
    # ----------------------------------------------------------------------
    monitor_list = monitor_arr.tolist()
    data = torch.tensor(monitor_arr[:, :p_dim], dtype=torch.float32)

    results = {
        "data": data,
        "metadata": {
            "description": "gLV parameter estimates trajectory over training epochs",
            "param_names": param_names,
            "true_vals": true_vals,
            "epochs": int(cfg.epochs),
        },
        "monitor": monitor_list,
        "param_names": param_names,
        "true_vals": true_vals,
        "hydra_cfg": OmegaConf.to_container(cfg, resolve=True),
    }

    results_path = run_dir / "glv_param_est_results.pt"
    torch.save(results, results_path)
    print(f"Results saved to {results_path}")

    # ----------------------------------------------------------------------
    # Also generate a gLV trajectory using the FINAL estimated parameters.
    # This mirrors the L63 experiment's "estimated trajectory" output.
    # ----------------------------------------------------------------------
    with torch.no_grad():
        # Integrate from the same initial state over the full experiment horizon.
        total_time = float(t_obs[-1].item())
        dt_integrate = dt
        t_eval = torch.arange(0.0, total_time + dt_integrate, dt_integrate, device=device)

        traj = odeint(
            learned_ode_func,
            initial_state,
            t_eval,
            method=cfg.ode_method,
            options=dict(step_size=cfg.ode_step_size),
        )

    traj_cpu = traj.detach().cpu()
    est_dataset = {
        "data": traj_cpu,
        "metadata": {
            "description": "gLV trajectory generated with estimated parameters from glv_param_est_run",
            "dt": dt_integrate,
            "initial_state": initial_state.detach().cpu().tolist(),
            "theta_labels": param_names,
            "theta_est": learned_ode_func.theta.detach().cpu().tolist(),
        },
    }

    est_path = run_dir / "glv_estimated_traj.pt"
    torch.save(est_dataset, est_path)
    print(f"Estimated gLV trajectory dataset saved to {est_path}")

    # ----------------------------------------------------------------------
    # Laplace approximation for EnKF-based posterior over gLV parameters
    # (files are named according to filter_mode: AD-EnKF vs EM-EnKF)
    # ----------------------------------------------------------------------
    from torch.autograd.functional import hessian

    theta_star = learned_ode_func.theta.detach().clone()
    p_dim = theta_star.numel()
    laplace_prefix = "ad_enkf" if filter_mode == "ad" else "em_enkf"

    def enkf_neg_log_likelihood(theta_flat: torch.Tensor) -> torch.Tensor:
        """
        Re-run EnKF with given theta and return negative log-likelihood.
        """
        theta_flat = theta_flat.to(device)
        theta_flat = theta_flat.requires_grad_(True)

        # Rebuild ODE with this theta
        ode_func = GLVParamODE(theta_flat, theta_labels, x_dim=x_dim).to(device)

        # Use same Q structure but with process_std from config
        q_scale_local = float(cfg.get("process_std", cfg.get("init_Q_diag_scale", 0.05)))
        init_Q_local = q_scale_local * torch.ones(x_dim, device=device)
        model_Q_local = noise.AddGaussian(x_dim, init_Q_local, "diag").to(device)

        N_ensem_local = N_ensem
        X_local = init_C_param(init_m.expand(train_size, N_ensem_local, x_dim))
        t_start_local = t0
        log_lik_accum = torch.zeros(train_size, device=device)

        for start in range(0, n_obs, L):
            end = min(start + L, n_obs)

            if filter_mode == "ad":
                # AD-EnKF: full adjoint EnKF
                X_local, _, log_likelihood = da_methods.EnKF(
                    ode_func,
                    true_obs_func,
                    t_obs[start:end],
                    y_obs[start:end],
                    N_ensem_local,
                    init_m,
                    init_C_param,
                    model_Q_local,
                    noise_R_true,
                    device,
                    save_filter_step={},
                    t0=t_start_local,
                    init_X=X_local,
                    ode_method=cfg.ode_method,
                    ode_options=dict(step_size=cfg.ode_step_size),
                    adjoint=True,
                    adjoint_method=cfg.adjoint_method,
                    adjoint_options=dict(step_size=cfg.adjoint_step_size),
                    tqdm=None,
                )
            else:
                # EM-EnKF: Term A only via EnKF_EM
                X_local, _, log_likelihood = EnKF_EM(
                    ode_func,
                    true_obs_func,
                    t_obs[start:end],
                    y_obs[start:end],
                    N_ensem_local,
                    init_m,
                    init_C_param,
                    model_Q_local,
                    noise_R_true,
                    device,
                    init_X=X_local,
                    ode_method=cfg.ode_method,
                    ode_options=dict(step_size=cfg.ode_step_size),
                    t0=float(t_start_local),
                    compute_likelihood=True,
                    tqdm=None,
                )

            t_start_local = t_obs[end - 1]
            log_lik_accum += log_likelihood

        neg_ll = -(log_lik_accum.mean())
        return neg_ll

    try:
        H = hessian(enkf_neg_log_likelihood, theta_star)
        H = H.detach()
        diag_H = torch.diag(H)
        if torch.any(diag_H <= 0):
            print("Warning: non-positive Hessian diagonal for gLV Laplace; skipping posterior save.")
        else:
            nugget = 1e-5 * torch.eye(p_dim, device=device, dtype=H.dtype)
            Sigma_post = torch.linalg.inv(H + nugget)
            std_post = torch.sqrt(torch.diag(Sigma_post).clamp(min=0)).detach().cpu().numpy()

            L_chol = torch.linalg.cholesky(Sigma_post + nugget)
            z = torch.randn(p_dim, 1000, device=device)
            theta_samples = (theta_star.to(device).unsqueeze(1) + L_chol @ z).T  # (1000, p_dim)
            theta_samples_np = theta_samples.detach().cpu().numpy()

            import numpy as _np

            _np.save(run_dir / f"{laplace_prefix}_laplace_theta_posterior.npy", theta_samples_np)
            _np.save(run_dir / f"{laplace_prefix}_laplace_std.npy", std_post)
            print(f"Saved gLV {laplace_prefix.upper().replace('_', '-')} Laplace posterior samples and std to run directory.")
    except RuntimeError as exc:
        print(f"Warning: Hessian computation for gLV Laplace failed with error: {exc}")


